////////////////////////// later /////////////////    
////////////////////////// later /////////////////    //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    // Local Variables Definition (local to this module)
////////////////////////// later /////////////////    //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    Camera camera = { 0 };
////////////////////////// later /////////////////    Vector3 cubePosition = { 0 };
////////////////////////// later /////////////////    
////////////////////////// later /////////////////    //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    // Local Functions Declaration
////////////////////////// later /////////////////    //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    static void UpdateDrawFrame(void);          // Update and draw one frame
////////////////////////// later /////////////////    
////////////////////////// later /////////////////    //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    // Main entry point
////////////////////////// later /////////////////    //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    int main()
////////////////////////// later /////////////////    {
////////////////////////// later /////////////////        // Initialization
////////////////////////// later /////////////////        //--------------------------------------------------------------------------------------
////////////////////////// later /////////////////        const int screenWidth = 800;
////////////////////////// later /////////////////        const int screenHeight = 450;
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        InitWindow(screenWidth, screenHeight, "raylib");
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        camera.position = (Vector3){ 10.0f, 10.0f, 8.0f };
////////////////////////// later /////////////////        camera.target = (Vector3){ 0.0f, 0.0f, 0.0f };
////////////////////////// later /////////////////        camera.up = (Vector3){ 0.0f, 1.0f, 0.0f };
////////////////////////// later /////////////////        camera.fovy = 60.0f;
////////////////////////// later /////////////////        camera.projection = CAMERA_PERSPECTIVE;
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        //--------------------------------------------------------------------------------------
////////////////////////// later /////////////////    
////////////////////////// later /////////////////    #if defined(PLATFORM_WEB)
////////////////////////// later /////////////////        emscripten_set_main_loop(UpdateDrawFrame, 60, 1);
////////////////////////// later /////////////////    #else
////////////////////////// later /////////////////        SetTargetFPS(60);               // Set our game to run at 60 frames-per-second
////////////////////////// later /////////////////        //--------------------------------------------------------------------------------------
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        // Main game loop
////////////////////////// later /////////////////        while (!WindowShouldClose())    // Detect window close button or ESC key
////////////////////////// later /////////////////        {
////////////////////////// later /////////////////            UpdateDrawFrame();
////////////////////////// later /////////////////        }
////////////////////////// later /////////////////    #endif
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        // De-Initialization
////////////////////////// later /////////////////        //--------------------------------------------------------------------------------------
////////////////////////// later /////////////////        CloseWindow();                  // Close window and OpenGL context
////////////////////////// later /////////////////        //--------------------------------------------------------------------------------------
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        return 0;
////////////////////////// later /////////////////    }
////////////////////////// later /////////////////    
////////////////////////// later /////////////////    // Update and draw game frame
////////////////////////// later /////////////////    static void UpdateDrawFrame(void)
////////////////////////// later /////////////////    {
////////////////////////// later /////////////////        // Update
////////////////////////// later /////////////////        //----------------------------------------------------------------------------------
////////////////////////// later /////////////////        UpdateCamera(&camera, CAMERA_ORBITAL);
////////////////////////// later /////////////////        //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        // Draw
////////////////////////// later /////////////////        //----------------------------------------------------------------------------------
////////////////////////// later /////////////////        BeginDrawing();
////////////////////////// later /////////////////    
////////////////////////// later /////////////////            ClearBackground(RAYWHITE);
////////////////////////// later /////////////////    
////////////////////////// later /////////////////            BeginMode3D(camera);
////////////////////////// later /////////////////    
////////////////////////// later /////////////////                DrawCube(cubePosition, 2.0f, 2.0f, 2.0f, RED);
////////////////////////// later /////////////////                DrawCubeWires(cubePosition, 2.0f, 2.0f, 2.0f, MAROON);
////////////////////////// later /////////////////                DrawGrid(10, 1.0f);
////////////////////////// later /////////////////    
////////////////////////// later /////////////////            EndMode3D();
////////////////////////// later /////////////////    
////////////////////////// later /////////////////            DrawText("This is a raylib example", 10, 40, 20, DARKGRAY);
////////////////////////// later /////////////////    
////////////////////////// later /////////////////            DrawFPS(10, 10);
////////////////////////// later /////////////////    
////////////////////////// later /////////////////        EndDrawing();
////////////////////////// later /////////////////        //----------------------------------------------------------------------------------
////////////////////////// later /////////////////    }
